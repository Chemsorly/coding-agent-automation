    /// <summary>
    /// Property 3: Private key base64 round-trip
    /// For any valid RSA private key in PKCS#1 format, base64-encoding the key bytes
    /// and then decoding them via the auth service SHALL produce an RSA key that can
    /// successfully sign data using RS256.
    /// **Validates: Requirements 2.2, 3.1**
    /// </summary>
    [Property(MaxTest = 20)]
    public void PrivateKey_Base64RoundTrip_CanDecodeAndSign(PositiveInt installationIdSeed)
    {
        // Arrange: Generate a fresh RSA key and export as PKCS#1 PEM
        using var rsa = RSA.Create(2048);
        var pemString = rsa.ExportRSAPrivateKeyPem();

        // Base64-encode the PEM string (this is how the key is stored in config)
        var pemBytes = Encoding.UTF8.GetBytes(pemString);
        var privateKeyBase64 = Convert.ToBase64String(pemBytes);

        // Use a deterministic client ID derived from the seed
        var clientId = $"Iv1.test{installationIdSeed.Get}";
        var installationId = (long)installationIdSeed.Get;
        var apiUrl = "https://api.github.com";

        // Act: Construct the auth service (this decodes base64 → PEM and validates)
        var authService = new GitHubAppAuthService(
            clientId,
            installationId,
            privateKeyBase64,
            apiUrl,
            MockLogger.Object);

        // Act: Generate a JWT (this imports the PEM key and signs with RS256)
        var jwt = authService.GenerateJwt();

        // Assert: JWT is non-empty and has three dot-separated parts (header.payload.signature)
        Assert.False(string.IsNullOrWhiteSpace(jwt));
        var parts = jwt.Split('.');
        Assert.Equal(3, parts.Length);

        // Assert: The JWT can be read back and the signature verified against the original key
        var handler = new JsonWebTokenHandler();
        var jsonWebToken = handler.ReadJsonWebToken(jwt);

        // Verify the issuer matches the client ID we provided
        Assert.Equal(clientId, jsonWebToken.Issuer);

        // Verify the algorithm is RS256
        Assert.Equal("RS256", jsonWebToken.Alg);

        // Verify the signature is valid by validating with the public key
        using var verifyRsa = RSA.Create();
        verifyRsa.ImportFromPem(pemString);
        var validationParameters = new TokenValidationParameters
        {
            ValidateIssuer = true,
            ValidIssuer = clientId,
            ValidateAudience = false,
            ValidateLifetime = false,
            IssuerSigningKey = new RsaSecurityKey(verifyRsa),
            RequireSignedTokens = true
        };

        var result = handler.ValidateTokenAsync(jwt, validationParameters).GetAwaiter().GetResult();
        Assert.True(result.IsValid, $"JWT signature validation failed: {result.Exception?.Message}");
    }

    /// <summary>
    /// Property 4: JWT structure correctness
    /// For any valid Client ID string and valid RSA private key, the JWT generated by
    /// GitHubAppAuthService.GenerateJwt() SHALL have: (a) the iss claim equal to the Client ID,
    /// (b) the iat claim within 61 seconds before the current UTC time, (c) the exp claim
    /// within 10 minutes and 1 second after the current UTC time, and (d) the alg header
    /// field equal to RS256.
    /// **Validates: Requirements 3.2, 3.3**
    /// </summary>
    [Property(MaxTest = 20)]
    public void Jwt_Structure_HasCorrectIssIatExpAlg(PositiveInt installationIdSeed)
    {
        // Arrange: Generate a fresh RSA key and base64-encode the PEM
        using var rsa = RSA.Create(2048);
        var pemString = rsa.ExportRSAPrivateKeyPem();
        var pemBytes = Encoding.UTF8.GetBytes(pemString);
        var privateKeyBase64 = Convert.ToBase64String(pemBytes);

        var clientId = $"Iv1.prop4test{installationIdSeed.Get}";
        var installationId = (long)installationIdSeed.Get;
        var apiUrl = "https://api.github.com";

        var authService = new GitHubAppAuthService(
            clientId,
            installationId,
            privateKeyBase64,
            apiUrl,
            MockLogger.Object);

        // Act: Capture time window around JWT generation
        var beforeGeneration = DateTimeOffset.UtcNow;
        var jwt = authService.GenerateJwt();
        var afterGeneration = DateTimeOffset.UtcNow;

        // Parse the JWT
        var handler = new JsonWebTokenHandler();
        var token = handler.ReadJsonWebToken(jwt);

        // Assert (a): iss claim equals the Client ID
        Assert.Equal(clientId, token.Issuer);

        // Assert (d): alg header equals RS256
        Assert.Equal("RS256", token.Alg);

        // Assert (b): iat is within 61 seconds before now
        // The service sets iat = now - 60s, so iat should be roughly (beforeGeneration - 60s) to (afterGeneration - 60s)
        var iat = token.IssuedAt;
        var iatOffset = new DateTimeOffset(iat, TimeSpan.Zero);
        // iat must be no more than 61 seconds before the time we started generating
        Assert.True(
            iatOffset >= beforeGeneration.AddSeconds(-61),
            $"iat ({iatOffset:O}) is more than 61 seconds before generation start ({beforeGeneration:O})");
        // iat must be no later than the time generation finished (it's backdated, so it should be before afterGeneration)
        Assert.True(
            iatOffset <= afterGeneration,
            $"iat ({iatOffset:O}) is after generation end ({afterGeneration:O})");

        // Assert (c): exp is within 10 minutes and 1 second after now
        var exp = token.ValidTo;
        var expOffset = new DateTimeOffset(exp, TimeSpan.Zero);
        // exp must be after the generation start time (it's set to now + 10m)
        Assert.True(
            expOffset >= beforeGeneration,
            $"exp ({expOffset:O}) is before generation start ({beforeGeneration:O})");
        // exp must be no more than 10 minutes and 1 second after generation end
        Assert.True(
            expOffset <= afterGeneration.AddMinutes(10).AddSeconds(1),
            $"exp ({expOffset:O}) is more than 10m1s after generation end ({afterGeneration:O})");

        // Additional structural check: exp - iat should be approximately 11 minutes (60s backdate + 10m forward)
        var duration = expOffset - iatOffset;
        Assert.True(
            duration.TotalMinutes >= 10.9 && duration.TotalMinutes <= 11.1,
            $"Duration between iat and exp should be ~11 minutes, was {duration.TotalMinutes:F2} minutes");
    }

