    /// <summary>
    /// Feature: github-app-auth, Property 8: Concurrent token acquisition serialization
    /// For any N concurrent calls to GetTokenAsync (where N >= 2) when the cache is
    /// pre-populated with a valid token (> 5 min remaining), all N callers SHALL receive
    /// the same token string. This verifies that the SemaphoreSlim serialization and
    /// shared cache work correctly under concurrent access — no caller sees a stale or
    /// null value, and no unnecessary API calls are made.
    /// **Validates: Requirements 4.4**
    /// </summary>
    [Property(MaxTest = 20, Arbitrary = [typeof(ConcurrentSerializationArbitrary)])]
    public void ConcurrentTokenAcquisition_AllCallersGetSameToken(ConcurrentSerializationInput input)
    {
        // Arrange: Create a valid auth service with a real RSA key
        using var rsa = RSA.Create(2048);
        var pemString = rsa.ExportRSAPrivateKeyPem();
        var pemBytes = Encoding.UTF8.GetBytes(pemString);
        var privateKeyBase64 = Convert.ToBase64String(pemBytes);

        var authService = new GitHubAppAuthService(
            "Iv1.concurrenttest",
            1L,
            privateKeyBase64,
            "https://api.github.com",
            MockLogger.Object);

        // Use reflection to pre-populate the cache with a valid token.
        // This simulates a previously successful token acquisition.
        // The expiry is > 5 minutes in the future, so the fast-path check succeeds.
        var cachedTokenField = typeof(GitHubAppAuthService)
            .GetField("_cachedToken", BindingFlags.NonPublic | BindingFlags.Instance)!;
        var tokenExpiresAtField = typeof(GitHubAppAuthService)
            .GetField("_tokenExpiresAt", BindingFlags.NonPublic | BindingFlags.Instance)!;

        cachedTokenField.SetValue(authService, input.TokenValue);
        tokenExpiresAtField.SetValue(authService, input.ExpiresAt);

        // Act: Fire N concurrent calls to GetTokenAsync.
        // All callers should return the same cached token without any API calls.
        // If the code attempted an API call, it would throw (no real GitHub API).
        var tasks = new Task<string>[input.ConcurrentCallers];
        using var barrier = new Barrier(input.ConcurrentCallers);

        for (var i = 0; i < input.ConcurrentCallers; i++)
        {
            tasks[i] = Task.Run(() =>
            {
                // Synchronize all threads to start at the same time.
                // Generous timeout to avoid flakiness on resource-constrained CI runners.
                barrier.SignalAndWait(TimeSpan.FromSeconds(60));
                return authService.GetTokenAsync(CancellationToken.None);
            });
        }

        // Wait for all concurrent calls to complete.
        // 120s is generous — the actual work is a fast cache read, but CI runners
        // can be slow to schedule 20 threads through a barrier.
        var allCompleted = Task.WhenAll(tasks).Wait(TimeSpan.FromSeconds(120));
        Assert.True(allCompleted, $"Not all {input.ConcurrentCallers} concurrent calls completed within 120 seconds");

        // Assert: All N callers received the exact same token string
        var results = tasks.Select(t => t.Result).ToArray();
        for (var i = 0; i < results.Length; i++)
        {
            Assert.Equal(input.TokenValue, results[i]);
        }

        // Assert: All results are reference-equal (same string instance from cache)
        for (var i = 1; i < results.Length; i++)
        {
            Assert.Same(results[0], results[i]);
        }

        // Assert: The cache was not modified (no renewal occurred)
        var currentCachedToken = (string?)cachedTokenField.GetValue(authService);
        Assert.Equal(input.TokenValue, currentCachedToken);
        Assert.Same(input.TokenValue, currentCachedToken);
    }

    /// <summary>
    /// Feature: github-app-auth, Property 7: Token caching — renewal trigger
    /// For any cached installation access token where (expiresAt - now) &lt;= 5 minutes,
    /// calling GetTokenAsync SHALL attempt to generate a new JWT and acquire a fresh
    /// installation access token from the GitHub API. Since no real API is available,
    /// the renewal attempt fails, but the service falls back to the cached token via
    /// graceful degradation — proving the renewal code path was triggered (the fast-path
    /// cache hit was NOT taken).
    /// **Validates: Requirements 4.3**
    /// </summary>
    [Property(MaxTest = 20, Arbitrary = [typeof(RenewalTriggerArbitrary)])]
    public void TokenCaching_RenewalTrigger_AttemptsRenewalWhenNearExpiry(RenewalTriggerInput input)
    {
        // Arrange: Create a valid auth service with a real RSA key and a MOCK client factory
        // that throws immediately — no real HTTP calls.
        using var rsa = RSA.Create(2048);
        var pemString = rsa.ExportRSAPrivateKeyPem();
        var pemBytes = Encoding.UTF8.GetBytes(pemString);
        var privateKeyBase64 = Convert.ToBase64String(pemBytes);

        var authService = new GitHubAppAuthService(
            "Iv1.renewaltest",
            1L,
            privateKeyBase64,
            "https://api.github.com",
            MockLogger.Object,
            CreateFailingClientFactory());

        // Use reflection to set the cached token and expiry directly.
        // The expiry is ≤ 5 minutes from now but still in the future,
        // so the fast-path check (> 5 min remaining) will FAIL,
        // triggering the renewal code path.
        var cachedTokenField = typeof(GitHubAppAuthService)
            .GetField("_cachedToken", BindingFlags.NonPublic | BindingFlags.Instance)!;
        var tokenExpiresAtField = typeof(GitHubAppAuthService)
            .GetField("_tokenExpiresAt", BindingFlags.NonPublic | BindingFlags.Instance)!;

        cachedTokenField.SetValue(authService, input.TokenValue);
        tokenExpiresAtField.SetValue(authService, input.ExpiresAt);

        // Act: Call GetTokenAsync. The flow is:
        // 1. Fast-path check: _tokenExpiresAt - _renewalBuffer <= now → FAILS (≤ 5 min remaining)
        // 2. Acquires semaphore, double-checks cache → same result
        // 3. Calls GenerateJwt() → succeeds (valid RSA key)
        // 4. Calls CreateInstallationToken → FAILS (no real GitHub API)
        // 5. Catch block: cached token still valid (not expired) → returns cached token
        //
        // This proves the renewal was triggered because:
        // - If the fast path were taken (like Property 6), no API call would be attempted
        // - The graceful degradation return proves the code entered the renewal path,
        //   attempted the API call, failed, and fell back to the cached token
        var result = authService.GetTokenAsync(CancellationToken.None).GetAwaiter().GetResult();

        // Assert: The returned token is the original cached token (graceful degradation)
        Assert.Equal(input.TokenValue, result);

        // Verify the cached token field still holds the original value
        // (renewal failed, so the cache was not updated with a new token)
        var currentCachedToken = (string?)cachedTokenField.GetValue(authService);
        Assert.Equal(input.TokenValue, currentCachedToken);

        // Verify the expiry was not updated (renewal failed)
        var currentExpiry = (DateTimeOffset)tokenExpiresAtField.GetValue(authService)!;
        Assert.Equal(input.ExpiresAt, currentExpiry);
    }

    /// <summary>
    /// Feature: github-app-auth, Property 9: Graceful degradation on renewal failure
    /// For any scenario where a cached token exists and has not yet expired (even if within
    /// the 5-minute renewal buffer) and the renewal attempt fails (API error, network error),
    /// GetTokenAsync SHALL return the existing cached token and SHALL NOT throw an exception.
    /// **Validates: Requirements 4.5**
    /// </summary>
    [Property(MaxTest = 20, Arbitrary = [typeof(RenewalTriggerArbitrary)])]
    public void GracefulDegradation_RenewalFailure_ReturnsCachedTokenWithoutThrowing(RenewalTriggerInput input)
    {
        // Arrange: Create a valid auth service with a real RSA key and a MOCK client factory
        // that throws immediately — no real HTTP calls.
        using var rsa = RSA.Create(2048);
        var pemString = rsa.ExportRSAPrivateKeyPem();
        var pemBytes = Encoding.UTF8.GetBytes(pemString);
        var privateKeyBase64 = Convert.ToBase64String(pemBytes);

        var authService = new GitHubAppAuthService(
            "Iv1.gracefultest",
            1L,
            privateKeyBase64,
            "https://api.github.com",
            MockLogger.Object,
            CreateFailingClientFactory());

        // Use reflection to set the cached token with expiry within the 5-minute
        // renewal buffer but still in the future (not yet expired).
        // This triggers the renewal path, which will fail (no real API),
        // and the service should gracefully fall back to the cached token.
        var cachedTokenField = typeof(GitHubAppAuthService)
            .GetField("_cachedToken", BindingFlags.NonPublic | BindingFlags.Instance)!;
        var tokenExpiresAtField = typeof(GitHubAppAuthService)
            .GetField("_tokenExpiresAt", BindingFlags.NonPublic | BindingFlags.Instance)!;

        cachedTokenField.SetValue(authService, input.TokenValue);
        tokenExpiresAtField.SetValue(authService, input.ExpiresAt);

        // Act & Assert: GetTokenAsync SHALL NOT throw any exception.
        // The Record.ExceptionAsync pattern captures any thrown exception;
        // we assert it is null to prove graceful degradation.
        var exception = Record.ExceptionAsync(() =>
            authService.GetTokenAsync(CancellationToken.None)).GetAwaiter().GetResult();

        Assert.Null(exception);

        // Act: Call GetTokenAsync again to get the actual return value
        // and verify it matches the cached token.
        var result = authService.GetTokenAsync(CancellationToken.None).GetAwaiter().GetResult();

        // Assert: The returned token is the original cached token (graceful fallback)
        Assert.Equal(input.TokenValue, result);

        // Assert: The cache still holds the original token (not replaced by a new one)
        var currentCachedToken = (string?)cachedTokenField.GetValue(authService);
        Assert.Equal(input.TokenValue, currentCachedToken);

        // Assert: The expiry was not updated (renewal failed, original expiry preserved)
        var currentExpiry = (DateTimeOffset)tokenExpiresAtField.GetValue(authService)!;
        Assert.Equal(input.ExpiresAt, currentExpiry);
    }
}

/// <summary>
/// Wrapper type for invalid private key inputs to enable FsCheck generation.
/// </summary>
public record InvalidPrivateKeyInput(string Value, string Category)
{
    public override string ToString() => $"[{Category}] {(Value.Length > 60 ? Value[..60] + "..." : Value)}";
}

/// <summary>
/// FsCheck Arbitrary that generates invalid private key inputs across three categories:
/// 1. Random non-base64 strings (contain characters invalid for base64)
/// 2. Truncated base64 (valid base64 key cut short)
/// 3. Valid base64 of non-RSA data (base64-encode random bytes that aren't a PEM key)
/// </summary>
public static class InvalidPrivateKeyArbitrary
{
    /// <summary>
    /// Generates a valid base64-encoded RSA PEM string for use in truncation tests.
    /// </summary>
    private static string GenerateValidBase64Key()
    {
        using var rsa = RSA.Create(2048);
        var pem = rsa.ExportRSAPrivateKeyPem();
        return Convert.ToBase64String(Encoding.UTF8.GetBytes(pem));
    }

    public static Arbitrary<InvalidPrivateKeyInput> InvalidPrivateKeyInput()
    {
        // Category 1: Random non-base64 strings
        // Generate strings with characters invalid for base64 (!, @, #, $, spaces, etc.)
        var invalidChars = new[] { '!', '@', '#', '$', '%', '^', '&', '*', '(', ')', '{', '}', '[', ']', '<', '>', '~', '`', '|', ';', ':', '\'', ',', '.', ' ', '\t' };
        var randomStrings =
            from len in Gen.Choose(1, 40)
            from chars in Gen.Elements(invalidChars).ArrayOf(len)
            from extraLen in Gen.Choose(0, 20)
            from extraChars in Gen.Elements(
                'a', 'b', 'c', 'A', 'B', 'C', '0', '1', '2', '!', '@', '#', '$', '%').ArrayOf(extraLen)
            let merged = new string(chars.Concat(extraChars).ToArray())
            select new InvalidPrivateKeyInput(merged, "RandomNonBase64");

        // Category 2: Truncated base64 — truncate to a non-multiple-of-4 length
        // which makes it invalid base64 (Convert.FromBase64String will throw)
        var truncatedBase64 =
            from baseLen in Gen.Choose(3, 30)
            // Ensure length is NOT a multiple of 4 (invalid base64 padding)
            let truncateLength = baseLen % 4 == 0 ? baseLen + 1 : baseLen
            let validBase64 = GenerateValidBase64Key()
            let truncated = validBase64[..Math.Min(truncateLength, validBase64.Length - 1)]
            select new InvalidPrivateKeyInput(truncated, "TruncatedBase64");

        // Category 3: Valid base64 of non-RSA data (random bytes encoded as base64)
        var nonRsaBase64 =
            from size in Gen.Choose(16, 256)
            from bytes in Gen.Choose(0, 255).ArrayOf(size)
            let base64 = Convert.ToBase64String(bytes.Select(b => (byte)b).ToArray())
            select new InvalidPrivateKeyInput(base64, "NonRsaBase64");

        // Combine all three categories with equal weight
        var combined = Gen.OneOf(randomStrings, truncatedBase64, nonRsaBase64);

        return combined.ToArbitrary();
    }
}

/// <summary>
/// Input type for cache hit property tests.
/// Contains a token string and an expiry time that is more than 5 minutes in the future.
/// </summary>
public record CacheHitInput(string TokenValue, DateTimeOffset ExpiresAt)
{
    public override string ToString() =>
        $"Token={TokenValue[..Math.Min(20, TokenValue.Length)]}..., ExpiresAt={ExpiresAt:O} (in {(ExpiresAt - DateTimeOffset.UtcNow).TotalMinutes:F1}m)";
}

/// <summary>
/// FsCheck Arbitrary that generates cache hit test inputs:
/// - Random non-empty token strings (simulating GitHub installation tokens)
/// - Expiry times that are strictly more than 5 minutes in the future
/// </summary>
public static class CacheHitArbitrary
{
    public static Arbitrary<CacheHitInput> CacheHitInput()
    {
        // Generate token strings: alphanumeric, 20-60 chars (similar to GitHub tokens)
        var tokenChars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_".ToCharArray();
        var tokenGen =
            from len in Gen.Choose(20, 60)
            from chars in Gen.Elements(tokenChars).ArrayOf(len)
            select "ghs_" + new string(chars);

        // Generate expiry times: 6 to 60 minutes in the future (always > 5 min buffer)
        var expiresAtGen =
            from minutesAhead in Gen.Choose(6, 60)
            select DateTimeOffset.UtcNow.AddMinutes(minutesAhead);

        var combined =
            from token in tokenGen
            from expiresAt in expiresAtGen
            select new CacheHitInput(token, expiresAt);

        return combined.ToArbitrary();
    }
}

