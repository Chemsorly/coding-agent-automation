/// </summary>
public record RenewalTriggerInput(string TokenValue, DateTimeOffset ExpiresAt)
{
    public override string ToString() =>
        $"Token={TokenValue[..Math.Min(20, TokenValue.Length)]}..., ExpiresAt={ExpiresAt:O} (in {(ExpiresAt - DateTimeOffset.UtcNow).TotalSeconds:F0}s)";
}

/// <summary>
/// FsCheck Arbitrary that generates renewal trigger test inputs:
/// - Random non-empty token strings (simulating GitHub installation tokens)
/// - Expiry times between 30 seconds and 5 minutes in the future
///   (≤ 5 min remaining triggers renewal, but still valid for graceful degradation)
/// </summary>
public static class RenewalTriggerArbitrary
{
    public static Arbitrary<RenewalTriggerInput> RenewalTriggerInput()
    {
        // Generate token strings: alphanumeric, 20-60 chars (similar to GitHub tokens)
        var tokenChars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_".ToCharArray();
        var tokenGen =
            from len in Gen.Choose(20, 60)
            from chars in Gen.Elements(tokenChars).ArrayOf(len)
            select "ghs_" + new string(chars);

        // Generate expiry times: 30 seconds to 300 seconds (5 minutes) in the future.
        // This ensures:
        // - The token is within the 5-minute renewal buffer (triggers renewal)
        // - The token has not yet expired (allows graceful degradation fallback)
        // - 30-second minimum provides enough time for the API call to fail
        //   and the graceful degradation check to still find a valid token
        var expiresAtGen =
            from secondsAhead in Gen.Choose(30, 300)
            select DateTimeOffset.UtcNow.AddSeconds(secondsAhead);

        var combined =
            from token in tokenGen
            from expiresAt in expiresAtGen
            select new RenewalTriggerInput(token, expiresAt);

        return combined.ToArbitrary();
    }
}
