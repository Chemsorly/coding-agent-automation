    #endregion

    #region Property 5: JWT issuer matches clientId

    /// <summary>
    /// Property 5: JWT issuer matches clientId
    /// For any valid clientId string and valid RSA private key, the JWT generated by
    /// GitHubAppAuthService SHALL contain the clientId as the `iss` (issuer) claim.
    /// **Validates: Requirements 10.2**
    /// </summary>
    [Property(MaxTest = 20)]
    public void JwtIssuer_MatchesClientId_ForAnyValidClientId(NonEmptyString clientIdInput)
    {
        // Arrange: Use the generated clientId and a valid RSA key
        var clientId = clientIdInput.Get;

        using var rsa = RSA.Create(2048);
        var pemString = rsa.ExportRSAPrivateKeyPem();
        var pemBytes = Encoding.UTF8.GetBytes(pemString);
        var privateKeyBase64 = Convert.ToBase64String(pemBytes);

        var sut = new GitHubAppAuthService(
            clientId,
            ValidInstallationId,
            privateKeyBase64,
            ValidApiUrl,
            MockLogger.Object);

        // Act: Generate a JWT
        var jwt = sut.GenerateJwt();

        // Assert: The JWT issuer claim matches the clientId exactly
        var handler = new JsonWebTokenHandler();
        var token = handler.ReadJsonWebToken(jwt);
        token.Issuer.Should().Be(clientId);
    }

    #endregion
}
