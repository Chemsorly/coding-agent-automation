    }

    #endregion

    #region Requirement 10.4: GetTokenAsync Returns Token from API Response

    /// <summary>
    /// Requirement 10.4: When token exchange succeeds, GetTokenAsync returns the token
    /// from the GitHub API response.
    /// </summary>
    [Fact]
    public async Task GetTokenAsync_SuccessfulExchange_ReturnsTokenFromApiResponse()
    {
        // Arrange
        var privateKeyBase64 = GenerateValidPrivateKeyBase64();
        var (factory, _, mockApps) = CreateMockClientFactory();

        var expectedToken = "ghs_test_installation_token_abc123";
        var expectedExpiry = DateTimeOffset.UtcNow.AddHours(1);

        var installationToken = new AccessToken(expectedToken, expectedExpiry);

        mockApps
            .Setup(a => a.CreateInstallationToken(ValidInstallationId))
            .ReturnsAsync(installationToken);

        var sut = new GitHubAppAuthService(
            ValidClientId,
            ValidInstallationId,
            privateKeyBase64,
            ValidApiUrl,
            MockLogger.Object,
            factory);

        // Act
        var result = await sut.GetTokenAsync(CancellationToken.None);

        // Assert
        result.Should().Be(expectedToken);
    }

    /// <summary>
    /// Requirement 10.4: The client factory is invoked with the correct API URL.
    /// </summary>
    [Fact]
    public async Task GetTokenAsync_InvokesClientFactoryWithCorrectApiUrl()
    {
        // Arrange
        var privateKeyBase64 = GenerateValidPrivateKeyBase64();
        Uri? capturedUri = null;

        var mockClient = new Mock<IGitHubClient>();
        var mockApps = new Mock<IGitHubAppsClient>();
        mockClient.Setup(c => c.GitHubApps).Returns(mockApps.Object);

        var installationToken = new AccessToken("token", DateTimeOffset.UtcNow.AddHours(1));
        mockApps
            .Setup(a => a.CreateInstallationToken(ValidInstallationId))
            .ReturnsAsync(installationToken);

        Func<string, Uri, IGitHubClient> factory = (jwt, uri) =>
        {
            capturedUri = uri;
            return mockClient.Object;
        };

        var sut = new GitHubAppAuthService(
            ValidClientId,
            ValidInstallationId,
            privateKeyBase64,
            ValidApiUrl,
            MockLogger.Object,
            factory);

        // Act
        await sut.GetTokenAsync(CancellationToken.None);

        // Assert
        capturedUri.Should().NotBeNull();
        capturedUri!.ToString().Should().Be(ValidApiUrl + "/");
    }

